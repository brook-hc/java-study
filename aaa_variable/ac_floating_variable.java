package aaa_variable;

public class ac_floating_variable {
    public static void main(String[] args) {
        //float（4字节），可以表示小数，但它的表示范围比int还要大。因为它用科学记数法表示数。
        //float定义变量时，要以f结尾。
        float id = 234.4534f;
        System.out.println(id);
        //double（8字节），比float范围更大，精度更高。
        //double定义时，末尾不需要加符号，日常都用double表示小数。
        double b = 243542.5645634332;
        System.out.println(b);
    }
}
