package aab_operator;

public class Aa_compare_operator {
    public static void main(String[] args) {
        int i =10;
        int j =20;

        System.out.println(i==j);  //判断是否相等
        System.out.println(i=j);   //j赋值给i，再输出i的值

        boolean n = true;
        boolean m = false;
        System.out.println(n==m);
        System.out.println(m=n);
    }

}
